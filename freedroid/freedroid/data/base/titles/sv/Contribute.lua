            HUR MAN BIDRAR

FreedroidRPG är helt och hållet fri programvara och fria illustrationer.
Det utvecklas av volontärer på deras fritid.

FreedroidRPG är beroende av sådana bidrag för att bli ett bättre spel.
Om du vill bidra med något, men inte är säker på vad, så följer här en lista på saker:

TESTANDE OCH BUGGRAPPORTER:

Vi behöver folk som testar spelet och rapporterar saker som inte fungerar som de ska eller verkar instabila. Det innefattar småsaker som stavningsfel och liknande. Arbetet att testa växer i och med att FreedroidRPG växer. Vi behöver din hjälp.
Tveka inte att skicka in en patch!

KARTSKAPANDE:

Vi söker efter personer som vill göra kartor åt oss. Det finns en inbyggd karteditor. Testa den, och om du skapat något intressant, se till att kontakta oss.

DOKUMENTATION:

Vi har för nuvarande brist på dokumentation. Vi skulle behöva en användarmanual till spelet och till kartredigeraren, såväl som dokumentation riktad till utvecklare. Bra skrivkunskaper krävs.

LJUDEFFEKTER:

Vi skulle gott behöva ljudeffekter. Om du vill bidra med några till ett gott ändamål kommer vi inte tacka nej. :)

KONTAKT:

Om du vill bidra med något eller ge feedback om spelet, kan du enkelt få kontakt med utvecklarna. Skicka bara ett brev till den här e-postadressen:
freedroid-discussion@lists.sourceforge.net

Eller, om du föredrar, kan du hoppa in i vår IRC-kanal (#freedroid på irc.freenode.net). Alla är välkommna. Ta med dig dina vänner!

Tack,

            Utvecklarna bakom FreedroidRPG.
]]
