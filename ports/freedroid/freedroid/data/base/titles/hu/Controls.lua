text = [[
            IRÁNYÍTÁS

MOZGÁS:

A játékot az egérrel lehet vezérelni. Kattints bal gombbal a helyre, ahova mennél. Tartsd lenyomva a gombot a folyamatosan abba az irányba mozgáshoz. A CTRL gomb lenyomásakor futni fogsz.

TÁMADÁS:

Az ellenfelekre bal kattintáskor Tux közelebb megy a bothoz, és elkezdi támadni a jelenlegi fegyverrel. Ha a jelenlegi fegyver lőfegyver, akkor Tux megáll, és tüzet nyit a botra.

KÜLÖNLEGES FUNKCIÓK:

Használj a jobb kattintást, hogy aktiváld a jelenleg kiválasztott képességet/programot.

Alapértelmezett gombok (a legtöbb gomb átállítható a játékon belül kiosztásban – F1)

  Escape … Főmenü megjelenítése
  F1… Kiosztás megjelenítése
  F2 … Teljes képernyő bekapcsolása (csak Linux)
  F3 … Gyors mentés
  F4 … Gyors betöltés
  F5-F12 … Programok gyors kiválasztása 1-től 8-ig

  I … Felszerelés panel
  C … Karakter panel
  S … Képességek/Program panel
  Szóköz … Minden nyitott panel bezárása
  Q … Küldetésnapló
  0-9 … Felszerelés gyors használata 0-tól 9-ig

  P … Szüneteltetés
  A nyomva tartva … Támadás ugyanabból a pozícióból
  Shift nyomva tartva … A jelenlegi célpont kiválasztva tartása mozgás közben
  Tab … Automatikus térkép bekapcsolása

  R … Fegyver újratöltése
  Ctrl nyomva tartva … Futás
  U … Állandó futás bekapcsolása

  X nyomva tartása … Tárgycímkék megjelenítése a földön
  Z … Tárgycímkék megjelenítésének bekapcsolása
  T … Falak átlátszóvá válásának bekapcsolása ha Tux közel megy (csak OpenGL)

ÁTVÉTEL/CSEVEGÉS

A barátságos droiddal beszélgetéshez elég ha rákattintasz. Az ellenséges bot vezérlésének átvételéhez válaszd ki az átvétel/csevegés képességet a képességek képernyőn, aztán kattints a jobb gombra az átvétel megkezdéséhez.

Az átvételi játékhoz az egérgörgőt vagy a kurzorbillentyűket használhatod.

Esetleg érdekelhet az Oktatójáték, amelyet a főmenüből tudsz elérni.

Ha elakadtál, és segítéségre van szükséged, akkor gyere fel a #freedroid csatornára az irc.freenode.net-en, és kérj segítséget.

Ennyi, jó játékot.



Nyomd meg a bal egérgombot a játék indításához.
]]
}
