title_screen{
song = "Bleostrada.ogg",
text = [[
            OVLÁDÁNÍ

POHYB:

Hra se ovládá pomocí myši. Klikněte levým tlačítkem na místa, do nichž se chcete přesunout. Podržte tlačítko, aby jste se pohybovali tímto směrem. Držením klávesnice CTRL budete utíkat.

ÚTOK:

Klikání levým tlačítkem myši na nepřítele způsobí, že se Tux přiblíží k robotovi a začne útočit s aktuální zbraní. Pokud je aktuální zbraní pistole, pak bude Tux stát na místě a střílet na robota.

SPECIÁLNÍ FUNKCE:

Použijte pravé tlačítko myši pro aktivaci aktuálně vybrané dovednosti/programu.

Výchozí klávesy (většina kláves může být přeřazena pomocí přehledu kláves v průběhu hry - F1)

  Klávesa ESC ... zobrazí hlavní menu
  F1 ... Přehled klávesových zkratek
  F2 ... Přepínání celé obrazovky (pouze Linux)
  F3 ... Rychlé uložení
  F4 ... Rychlé nahrání
  F5-F12 ... Rychlý výběr programu od 1 do 8

  I ... Panel inventáře
  C ... Panel postavy
  S ... Panel dovedností/programů
  Mezerník ... Zavře všechny otevřené panely
  Q ... Přehled úkolů
  0-9 ... Rychlé použití položek 0-9 z inventáře

  P ... Pauza
  Drž A ... Útok při zachování stejné pozice
  Drž Shift ... Aktuální cíl zůstane při pohybu vybrán
  Tab ... Přepínání automapy

  R ... Nabít zbraň
  Drž CTRL ... Běh
  U ... Přepínání běhu a chůze

  Drž X ... Zobraz názvy předmětů na zemi
  Z ... Přepínání zobrazení názvů předmětů na zemi
  T ... Přepínání průhlednost zdí, kde Tux prochází (pouze OpenGL)

PŘEVZETÍ/CHAT

Chcete-li konverzovat s přátelským droidem, stačí, abyste na něj klikli. Pro převzetí vlády nad nepřátelským robotem vyberte dovednost převzetí/chat z panelu dovedností, pak klepněte pravým tlačítkem myši pro zahájení převzetí.

V průběhu hry převzetí lze použít kolečko myši nebo kurzorová tlačítka.

Možná vás bude zajímat výuková hra, která je přístupná z hlavního menu.

Pokud jste se zasekli a potřebujete pomoc, neváhejte přijít na #freedroid na irc.freenode.net a požádejte o pomoc.

To je vše, užijte si hru.



Stiskem levého tlačítka myši začněte hrát.
]]
}
